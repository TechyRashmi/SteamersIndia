package com.blucor.steamersindia.model;

public class SpareModel {

    private String spare_id;
    private String updated_quantity;
    private String spare_name;
    private String capacity;
    private String size;


    public String getSpare_id() {
        return spare_id;
    }

    public void setSpare_id(String spare_id) {
        this.spare_id = spare_id;
    }

    public String getUpdated_quantity() {
        return updated_quantity;
    }

    public void setUpdated_quantity(String updated_quantity) {
        this.updated_quantity = updated_quantity;
    }

    public String getSpare_name() {
        return spare_name;
    }

    public void setSpare_name(String spare_name) {
        this.spare_name = spare_name;
    }

    public String getCapacity() {
        return capacity;
    }

    public void setCapacity(String capacity) {
        this.capacity = capacity;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
}
